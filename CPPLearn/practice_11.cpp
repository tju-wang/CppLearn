#include <iostream>
using namespace std;
#include <string>
#include <algorithm>
#include <sstream>
/*
描述：

输入一个整数，将这个整数以字符串的形式逆序输出

程序不考虑负数的情况，若数字含有0，则逆序形式也含有0，如输入为100，则输出为001
*/

int main()
{
	int num;
	cin >> num;
	string str = to_string(num);
	reverse(str.begin(), str.end());
	cout << str << endl;
	

	return 0;
}